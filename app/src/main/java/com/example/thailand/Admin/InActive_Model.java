package com.example.thailand.Admin;

public class InActive_Model {
    String id,name;

    public InActive_Model() {
    }

    public InActive_Model(String id, String name) {
        this.id = id;
        this.name = name;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
